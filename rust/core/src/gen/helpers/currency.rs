// ISO 4217
pub fn locale_to_currency(code: &str) -> &'static str {
    match code {
        "de" | "de-at" | "fr" | "fr-be" | "it" | "es" | "nl" | "nl-be" | "pt" | "ie" | "el"
        | "cy" | "fi" | "sk" | "sl" | "et" | "lt" | "lv" | "hr" | "mt" | "lb" => "EUR",
        "en-gb" => "GBP",
        "en-ca" | "fr-ca" => "CAD",
        "en-au" => "AUD",
        "en-nz" => "NZD",
        "en-sg" => "SGD",
        "en-za" => "ZAR",
        "en-ng" => "NGN",
        "ja" => "JPY",
        "zh" => "CNY",
        "hi" => "INR",
        "pt-br" => "BRL",
        "se" => "SEK",
        "da" => "DKK",
        "no" => "NOK",
        "pl" => "PLN",
        "cs" => "CZK",
        "hu" => "HUF",
        "ro" => "RON",
        "bg" => "BGN",
        "ru" => "RUB",
        "sr" => "RSD",
        "uk" => "UAH",
        "be" => "BYN",
        "tr" => "TRY",
        "ar" => "ARS",
        "mx" => "MXN",
        "cl" => "CLP",
        "co" => "COP",
        "pe" => "PEN",
        "uy" => "UYU",
        "ve" => "VES",
        "vi" => "VND",
        "ko" => "KRW",
        "id" => "IDR",
        "th" => "THB",
        "ms" => "MYR",
        "tl" => "PHP",
        "tw" => "TWD",
        "he" => "ILS",
        "ar-sa" => "SAR",
        "ar-ae" => "AED",
        "pk" => "PKR",
        "bd" => "BDT",
        "eg" => "EGP",
        _ => "USD",
    }
}
