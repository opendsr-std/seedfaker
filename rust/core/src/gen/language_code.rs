use crate::ctx::GenContext;

pub fn gen(ctx: &mut GenContext<'_>, buf: &mut String) {
    let loc = ctx.locale();
    let code = match loc.code {
        "de" | "de-at" => "de",
        "fr" | "fr-be" | "fr-ca" => "fr",
        "it" => "it",
        "es" | "ar" | "mx" | "cl" | "co" | "pe" | "uy" | "ve" | "ec" => "es",
        "nl" | "nl-be" => "nl",
        "pt" | "pt-br" => "pt",
        "ja" => "ja",
        "zh" | "tw" => "zh",
        "hi" => "hi",
        "vi" => "vi",
        "tr" => "tr",
        "se" => "sv",
        "da" => "da",
        "no" => "nb",
        "fi" => "fi",
        "pl" => "pl",
        "cs" => "cs",
        "sk" => "sk",
        "hu" => "hu",
        "ro" => "ro",
        "hr" => "hr",
        "bg" => "bg",
        "sr" => "sr",
        "ru" => "ru",
        "uk" => "uk",
        "be" => "be",
        "el" | "cy" => "el",
        "sl" => "sl",
        "et" => "et",
        "lt" => "lt",
        "lv" => "lv",
        "ko" => "ko",
        "id" => "id",
        "th" => "th",
        "ms" => "ms",
        "tl" => "tl",
        "he" => "he",
        "ar-sa" | "ar-ae" | "eg" => "ar",
        "pk" => "ur",
        "bd" => "bn",
        "mt" => "mt",
        "lb" => "lb",
        _ => "en",
    };
    buf.push_str(code);
}
